package com.example.game.Quarto.misc;

/**
 * New Point class because normal java Point isn't working.
 */
public class NewPoint {
    public int x; // x location
    public int y; // y location

    /**
     * Constructor for Point
     *
     * @param x x location of new point
     * @param y y location of new point
     */
    public NewPoint(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}
